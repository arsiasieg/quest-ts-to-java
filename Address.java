public class Address {
    int number;
    String street;
    String city;
    String country;
    
    public int getNumber() {
    	return number;
    }
    public String getStreet() {
    	return street;
    }
    public String getCity() {
    	return city;
    }
    public String getCountry() {
    	return country;
    }
    
}