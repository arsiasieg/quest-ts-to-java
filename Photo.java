

public class Photo {
    String url;
    String filename;
    double width;
    double height;
    
    public String getUrl() {
    	return url;
    }
    public String getFilename() {
    	return filename;
    }
    public double getWidth() {
    	return width;
    }
    public double getHeight() {
    	return height;
    }
    
}